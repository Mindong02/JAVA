public class string {
	public static void main(String[] args) {
		System.out.println("Hello world!"); //string
		System.out.println("hello world!");// char
		System.out.println('H'+'I');
		System.out.println("Hello " + "world!");
		System.out.println("hello \nworld!");
		System.out.println("hello \"world\"");
	}
}
 